#printing numbers does not require quotes
print(9)
print(1.5)

#printing multiple values using single print statement
print("my age is",18)
print("aasim","john")
print(43,5)

#printing using seperator
print(3,3,sep=".")

#printing without newline
print("mohamed",end=" ")
print("äasim")

#empty statement
print()
print("--")

#printing special characters
print("hello\naasim")#creates new line
print("hello\taasim")#creates a tabspace

#basic mathematical operations using print statemnet
print(9+3)
print(5-3)
print(8*7)
print(6/3)

#prints in the next line  IMPORTANT
print("mohamed",end="\n")
print("äasim")


                                                                                                                                      